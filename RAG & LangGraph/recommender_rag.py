# 1. Load the embedding model (Person 3 used all-MiniLM-L6-v2)
print("Loading embedding model...")
embedding_model = SentenceTransformer("all-MiniLM-L6-v2")

def group_and_rank_reviews(chroma_results, min_similarity=0.3):
    """
    Takes ChromaDB results, groups chunks by restaurant, and scores them.
    Score = (Number of reviews matching vibe) * (Restaurant Star Rating)
    """
    restaurants = {}

    # Chroma returns lists of lists. We grab the first index [0]
    if not chroma_results or not chroma_results['documents'] or len(chroma_results['documents'][0]) == 0:
        return []

    docs = chroma_results['documents'][0]
    metas = chroma_results['metadatas'][0]
    distances = chroma_results['distances'][0]

    for doc, meta, dist in zip(docs, metas, distances):
        # Convert distance to similarity (Assuming Cosine distance: 1 - dist = similarity)
        similarity = 1 - dist

        # Only keep reviews that actually match the user's vibe well
        if similarity < min_similarity:
            continue

        b_id = meta['business_id']
        if b_id not in restaurants:
            restaurants[b_id] = {
                "business_id": b_id,
                "restaurant_name": meta.get("restaurant_name", "Unknown"),
                "stars": meta.get("rating", 0.0), # Note: using 'rating' from your sample metadata
                "price_range": meta.get("price_range", 2),
                "hit_count": 0,
                "reviews": []
            }

        restaurants[b_id]["hit_count"] += 1
        restaurants[b_id]["reviews"].append(doc)

    # Calculate final score: (Hits) * (Stars)
    ranked_list = list(restaurants.values())
    for r in ranked_list:
        r["final_score"] = r["hit_count"] * r["stars"]

    # Sort highest score first
    ranked_list.sort(key=lambda x: x["final_score"], reverse=True)
    return ranked_list

def two_step_retrieval(query_dict: dict, primary_business_ids: list[str]) -> dict:
    cuisines = " ".join(query_dict.get("cuisine", []))
    raw_query = query_dict.get("original_query", " ".join(query_dict.get("vibe", [])))

    search_text = f"{cuisines} food. {raw_query}"
    query_vector = embedding_model.encode(search_text).tolist()
    user_price = query_dict.get("price_range", 2)

    top_primary = []

    # === FIX: Only run Primary Search if we actually have community IDs ===
    if primary_business_ids:
        primary_where = {
            "$and": [
                {"business_id": {"$in": primary_business_ids}},
                {"price_range": {"$eq": user_price}},
                {"rating": {"$gte": 3.5}}
            ]
        }
        primary_results = collection.query(
            query_embeddings=[query_vector], n_results=30, where=primary_where, include=["documents", "metadatas", "distances"]
        )
        top_primary = group_and_rank_reviews(primary_results)[:6]

    # === FIX: Safe Wildcard Search ===
    if primary_business_ids:
        wildcard_where = {
            "$and": [
                {"business_id": {"$nin": primary_business_ids}},
                {"price_range": {"$eq": user_price}},
                {"rating": {"$gte": 4.0}}
            ]
        }
    else:
        # If no cuisine was given, search the entire city!
        wildcard_where = {
            "$and": [
                {"price_range": {"$eq": user_price}},
                {"rating": {"$gte": 4.0}}
            ]
        }

    wildcard_results = collection.query(
        query_embeddings=[query_vector], n_results=20, where=wildcard_where, include=["documents", "metadatas", "distances"]
    )

    # If there were no primary matches, let the wildcards take all 5 slots
    slots_to_fill = 4 if primary_business_ids else 5
    top_wildcards = group_and_rank_reviews(wildcard_results)[:slots_to_fill]

    return {"primary_recommendations": top_primary, "wildcard_recommendations": top_wildcards}

