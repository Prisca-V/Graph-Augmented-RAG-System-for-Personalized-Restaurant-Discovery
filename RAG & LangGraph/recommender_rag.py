def get_primary_community_ids(query: dict) -> list[str]:
    """
    Finds ALL communities that match the requested cuisine using a flexible substring match.
    Returns a combined list of business_ids belonging to those primary communities.
    """
    cuisines = [c.lower() for c in query.get("cuisine", [])]

    if not cuisines:
        print("No cuisine provided. Defaulting to global search.")
        return []

    matched_communities = []

    # Score each community and keep ALL that have at least 1 match
    for _, row in summary_df.iterrows():
        comm_id = int(row["community_id"])

        if comm_id == -1:
            continue

        tags_raw = str(row.get("top_5_tags", "")).lower()

        # Check if any user cuisine word is in the community's tags
        score = sum(1 for c in cuisines if c in tags_raw or tags_raw.find(c) != -1)

        if score > 0:
            matched_communities.append(comm_id)

    # If we found matching communities, get ALL their business IDs
    if matched_communities:
        print(f"Matched Cuisine to Community IDs: {matched_communities}")

        # Use .isin() to grab restaurants from all matching communities
        business_ids = mapping_df[mapping_df["community_id"].isin(matched_communities)]["business_id"].tolist()
        print(f"Found {len(business_ids)} primary restaurants across these communities.")

        return business_ids

    print("No specific community matched the cuisine. Defaulting to global search.")
    return []

