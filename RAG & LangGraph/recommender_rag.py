def retrieve_and_synthesize(query_dict: dict) -> list[dict]:
    print("Starting RAG Pipeline...")

    # 1. Graph Routing
    primary_ids = get_primary_community_ids(query_dict)

    # 2. Semantic Search
    retrieved_data = two_step_retrieval(query_dict, primary_ids)

    print("\n" + "="*50)
    print("X-RAY: WHAT CHROMADB FOUND (BEFORE GEMINI)")
    print("="*50)

    print("PRIMARY POOL (Safe Bets):")
    if not retrieved_data["primary_recommendations"]:
        print("   None found!")
    for i, r in enumerate(retrieved_data["primary_recommendations"]):
        print(f"   {i+1}. {r['restaurant_name']} | {r['stars']} | Hits: {r['hit_count']} | Score: {r['final_score']}")

    print("\nWILDCARD POOL (Surprises):")
    if not retrieved_data["wildcard_recommendations"]:
        print("   None found!")
    for i, r in enumerate(retrieved_data["wildcard_recommendations"]):
        print(f"   {i+1}. {r['restaurant_name']} | {r['stars']} | Hits: {r['hit_count']} | Score: {r['final_score']}")
    print("="*50 + "\n")
    # ==========================================
    total = len(retrieved_data["primary_recommendations"]) + len(retrieved_data["wildcard_recommendations"])

    if total == 0:
        return {"error": "No restaurants found matching your preferences. Try adjusting your price range or cuisine."}

    # 3. LLM Synthesis
    final_output = synthesize_with_llm(retrieved_data, query_dict)

    print(" Pipeline Complete.")
    return final_output

