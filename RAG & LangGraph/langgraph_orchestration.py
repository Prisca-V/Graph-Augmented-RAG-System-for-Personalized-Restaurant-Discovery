# 1. STATE
class RestaurantState(TypedDict):
    messages: Annotated[list[dict], operator.add]
    original_query: str
    cuisine: list[str]
    vibe: list[str]
    price_range: int | None
    party_size: int | None
    group_type: str | None
    dietary: list[str]
    must_have: list[str]
    avoid: list[str]
    neighborhood_or_landmark: str | None
    city: str
    open_requirement: str
    is_complete: bool
    followup_question: str | None
    missing_fields: list[str]
    followup_count: int
    final_recommendations: list[dict]
    error: str | None

def get_default_state() -> RestaurantState:
    return {
        "messages": [], "original_query": "", "cuisine": [], "vibe": [],
        "price_range": None, "party_size": None, "group_type": None,
        "dietary": [], "must_have": [], "avoid": [],
        "neighborhood_or_landmark": None, "city": "Philadelphia",
        "open_requirement": "none", "is_complete": False,
        "followup_question": None, "missing_fields": [],
        "followup_count": 0, "final_recommendations": [], "error": None
    }

# 2. NODES
def parse_input(state: RestaurantState) -> RestaurantState:
    latest_message = next((msg["content"] for msg in reversed(state["messages"]) if msg["role"] == "user"), "")
    if not latest_message: return state
    if not state["original_query"]: state["original_query"] = latest_message

    prompt = f"""Extract structured attributes from the user message.
    User message: "{latest_message}"
    Return ONLY a valid JSON object with keys: cuisine (list), vibe (list), price_range (int 1-4 or null), party_size, group_type, dietary, must_have, avoid."""

    try:
        response = llm.invoke(prompt)

        # --- THE FIX: Safely handle if LangChain returns a list ---
        raw_content = response.content
        if isinstance(raw_content, list):
            # Pull the text out of the list
            raw_content = raw_content[0]["text"] if isinstance(raw_content[0], dict) else str(raw_content[0])

        raw = str(raw_content).strip().replace("```json", "").replace("```", "").strip()
        parsed = json.loads(raw)

        for key in ["cuisine", "vibe", "price_range", "party_size", "group_type", "dietary", "must_have", "avoid"]:
            if parsed.get(key) is not None and parsed.get(key) != []:
                state[key] = parsed[key]
    except Exception as e:
        print(f"Parse Error: {e}")
    return state

def check_completeness(state: RestaurantState) -> RestaurantState:
    missing = []
    if not state["cuisine"]: missing.append("cuisine")
    if state["price_range"] is None: missing.append("price_range")
    state["missing_fields"] = missing
    state["is_complete"] = len(missing) == 0

    # FIX: Define the question HERE so it saves to the state before the graph pauses!
    if not state["is_complete"]:
        questions = {
            "cuisine": "What kind of food or cuisine are you in the mood for?",
            "price_range": "What price range are you thinking? ($, $$, $$$, or $$$$)"
        }
        state["followup_question"] = next((questions[f] for f in ["cuisine", "price_range"] if f in missing), "Can you tell me more about what you want?")
    else:
        state["followup_question"] = None

    return state

def ask_followup(state: RestaurantState) -> RestaurantState:
    # We grab the question we already saved in the previous node
    question = state["followup_question"]
    state["followup_count"] = state.get("followup_count", 0) + 1

    # Pause the graph and wait for the frontend!
    user_reply = interrupt(question)
    return {"messages": [{"role": "user", "content": user_reply}]}

