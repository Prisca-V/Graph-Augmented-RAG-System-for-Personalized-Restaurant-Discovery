# 3. BUILD GRAPH
builder = StateGraph(RestaurantState)
builder.add_node("parse_input", parse_input)
builder.add_node("check_completeness", check_completeness)
builder.add_node("ask_followup", ask_followup)
builder.add_node("retrieve", retrieve)
builder.add_node("format_response", format_response)

builder.add_edge(START, "parse_input")
builder.add_edge("parse_input", "check_completeness")
builder.add_conditional_edges("check_completeness", route_after_completeness_check, {"ask_followup": "ask_followup", "retrieve": "retrieve"})
builder.add_edge("ask_followup", "parse_input") # Loops back to read answer
builder.add_edge("retrieve", "format_response")
builder.add_edge("format_response", END)

graph = builder.compile(checkpointer=MemorySaver())

# 4. CHAT RUNNER (Returns the exact Frontend JSON Contract)
def run_conversation(user_message: str, graph, thread_id: str = "default") -> dict:
    config = {"configurable": {"thread_id": thread_id}}
    current_state = graph.get_state(config)

    if current_state.next == () or current_state.values == {}:
        initial_state = get_default_state()
        initial_state["messages"] = [{"role": "user", "content": user_message}]
        result = graph.invoke(initial_state, config)
    else:
        result = graph.invoke(Command(resume=user_message), config)

    api_response = {
        "matched_community": " & ".join(result.get("cuisine", [])) if result.get("cuisine") else "Global Search",
        "city": result.get("city", "Philadelphia"),
        "restaurants": result.get("final_recommendations", []),
        "follow_up_question": result.get("followup_question", None)
    }

    if result.get("is_complete") and result.get("final_recommendations"):
         api_response["follow_up_question"] = None

    return api_response

